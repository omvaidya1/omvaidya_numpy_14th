import numpy as np

# Create arrays
a = np.array([10, 20, 30, 40])
b = np.array([1, 2, 3, 4])

print("Array a:", a)
print("Array b:", b)

# Arithmetic operations
print("\nAddition (a + b):", a + b)
print("Subtraction (a - b):", a - b)
print("Multiplication (a * b):", a * b)
print("Division (a / b):", a / b)

# Element-wise power
print("\nPower (a ** b):", a ** b)

# Modulo operation
print("Modulo (a % b):", a % b)